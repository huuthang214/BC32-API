function Users(id, account, name, password, email, image, typeUser, language, description) {
    this.id = id;
    this.account = account;
    this.name = name;
    this.password = password;
    this.email = email;
    this.image = image;
    this.typeUser = typeUser;
    this.language = language;
    this.description = description;
}